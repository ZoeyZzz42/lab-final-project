    private void CartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CartButtonActionPerformed
        ShoppingCart newSc = new ShoppingCart();
        int selectedIdx = userTable.getSelectedRow();
        try{
            
            selectedProduct = products.get(selectedIdx);
            newSc.setProductName(selectedProduct.getProductName());
            newSc.setPrice(selectedProduct.getPrice());
            newSc.setProductId(UUID.randomUUID().toString());
            DatabaseConnector.addShoppingCart(newSc);
        } catch (Exception ex)  {
            JOptionPane.showMessageDialog(this, "Please enter correct details", "Error", HEIGHT);
        }

//        products.addProduct(newProduct);
//        System.out.println(products);
        JOptionPane.showMessageDialog(this, "Product Add into Cart", "Success", HEIGHT);

    }//GEN-LAST:event_CartButtonActionPerformed

    public void populateTable() {
        try{
            this.products = DatabaseConnector.getAllusers();
            DefaultTableModel model = (DefaultTableModel) userTable.getModel();
            model.setRowCount(0);
            for (Product u:products){
                Object[] row = new Object[3];
//                row[0] = u.getProductId();
                row[0] = u.getProductName();
                row[1] = u.getPrice();
                row[2] = u.getProductDescription();
                model.addRow(row);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
}
